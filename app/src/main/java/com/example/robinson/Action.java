package com.example.robinson;

public class Action {
    String name;
    String contain;
    int changedFood;
    int changedWater;
    int changedMaterials;

    public Action(String name, String contain, int changedFood, int changedWater, int changedMaterials) {
        this.name = name;
        this.contain = contain;
        this.changedFood = changedFood;
        this.changedWater = changedWater;
        this.changedMaterials = changedMaterials;
    }


}
